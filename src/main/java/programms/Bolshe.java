package programms;

public class Bolshe {


       // Метод для проверки, что a больше b
       public boolean aBolsheB(int a, int b) {
           return a > b;
       }

       // Метод для проверки, что a меньше b
       public boolean bBolsheA(int a, int b) {
           return a < b;
       }

       // Метод для проверки, что a равно b
       public boolean aRavnoB(int a, int b) {
           return a == b;
       }

   }