package programms;

public class Bolshe {


       // Метод для проверки, что a больше b
       public boolean isAGreaterThanB(int a, int b) {
           return a > b;
       }

       // Метод для проверки, что a меньше b
       public boolean isALessThanB(int a, int b) {
           return a < b;
       }

       // Метод для проверки, что a равно b
       public boolean isAEqualToB(int a, int b) {
           return a == b;
       }

   }
